// 문제 좀 더 확장해서 ( ) 사이에 아무 글자가 있을 수 있다고 가정
export function stack(text: string): boolean {
  /**
   * const charStacks = []; // 문자열 ( )만 저장할 수 있음.
   *
   * text 한글자씩 순회
   * 만약 ( 면  stack에 넣기
   * 만약 ) 면 stack 젤 위에 (가 있다면 pop 하는걸로 continue, 그 외 ')'가 있었거나 스택에 뭐가 없는경우 = return false
   * 그 외 = continue;
   *
   * 만약 stack에 뭔가 남아있다 ==> return false = ( ) 갯수가 똑같지가않음 = 짝 안지어짐
   * 하나도 없다 ==> return true
   */
  return true;
}
